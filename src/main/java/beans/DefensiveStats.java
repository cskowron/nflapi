public class DefensiveStats {

    private int combineTackles;
    private int totalTackles;
    private int assistTackles;
    private float sacks;
    private int forcedFumbles;
    private int safeties;
    private int interceptions;
    private int interceptionsTouchdowns;
    private int interceptionsYards;
    private float interceptionsYardsAvg;
    private int interceptionsLong;
    private boolean interceptionsLongTouchdown;
    private int passesDefensed;
    private int fgBlocked;
    private int puntBlocked;
    private int xpBlocked;

    public int getCombineTackles() {
        return combineTackles;
    }

    public void setCombineTackles(int combineTackles) {
        this.combineTackles = combineTackles;
    }

    public int getTotalTackles() {
        return totalTackles;
    }

    public void setTotalTackles(int totalTackles) {
        this.totalTackles = totalTackles;
    }

    public int getAssistTackles() {
        return assistTackles;
    }

    public void setAssistTackles(int assistTackles) {
        this.assistTackles = assistTackles;
    }

    public float getSacks() {
        return sacks;
    }

    public void setSacks(float sacks) {
        this.sacks = sacks;
    }

    public int getForcedFumbles() {
        return forcedFumbles;
    }

    public void setForcedFumbles(int forcedFumbles) {
        this.forcedFumbles = forcedFumbles;
    }

    public int getSafeties() {
        return safeties;
    }

    public void setSafeties(int safeties) {
        this.safeties = safeties;
    }

    public int getInterceptions() {
        return interceptions;
    }

    public void setInterceptions(int interceptions) {
        this.interceptions = interceptions;
    }

    public int getInterceptionsTouchdowns() {
        return interceptionsTouchdowns;
    }

    public void setInterceptionsTouchdowns(int interceptionsTouchdowns) {
        this.interceptionsTouchdowns = interceptionsTouchdowns;
    }

    public int getInterceptionsYards() {
        return interceptionsYards;
    }

    public void setInterceptionsYards(int interceptionsYards) {
        this.interceptionsYards = interceptionsYards;
    }

    public float getInterceptionsYardsAvg() {
        return interceptionsYardsAvg;
    }

    public void setInterceptionsYardsAvg(float interceptionsYardsAvg) {
        this.interceptionsYardsAvg = interceptionsYardsAvg;
    }

    public int getInterceptionsLong() {
        return interceptionsLong;
    }

    public void setInterceptionsLong(int interceptionsLong) {
        this.interceptionsLong = interceptionsLong;
    }

    public boolean isInterceptionsLongTouchdown() {
        return interceptionsLongTouchdown;
    }

    public void setInterceptionsLongTouchdown(boolean interceptionsLongTouchdown) {
        this.interceptionsLongTouchdown = interceptionsLongTouchdown;
    }

    public int getPassesDefensed() {
        return passesDefensed;
    }

    public void setPassesDefensed(int passesDefensed) {
        this.passesDefensed = passesDefensed;
    }

    public int getFgBlocked() {
        return fgBlocked;
    }

    public void setFgBlocked(int fgBlocked) {
        this.fgBlocked = fgBlocked;
    }

    public int getPuntBlocked() {
        return puntBlocked;
    }

    public void setPuntBlocked(int puntBlocked) {
        this.puntBlocked = puntBlocked;
    }

    public int getXpBlocked() {
        return xpBlocked;
    }

    public void setXpBlocked(int xpBlocked) {
        this.xpBlocked = xpBlocked;
    }
}
